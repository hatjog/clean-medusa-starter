async function ensureMarketSellerFulfillmentZones(
  container: ExecArgs["container"],
  db: Knex,
  sellers: MarketSellerRow[],
  countries: string[],
  dryRun: boolean
) {
  const sellerNames = sellers.map(seller => seller.seller_name).filter(Boolean)
  const countriesForZones = countries.length ? countries : ["PL"]
  const existingLocations = await listStockLocationsByNames(db, sellerNames)
  const existingLocationNames = new Set(existingLocations.map(location => location.name))
  const missingSellers = sellers.filter(seller => !existingLocationNames.has(seller.seller_name))

  let createdStockLocations = 0
  if (missingSellers.length > 0) {
    if (dryRun) {
      createdStockLocations = missingSellers.length
    } else {
      await createStockLocationsWorkflow(container).run({
        input: {
          locations: missingSellers.map(seller => ({
            name: seller.seller_name,
            metadata: {
              gp: {
                seller_id: seller.seller_id,
                seller_handle: seller.seller_handle,
                autogenerated: true,
                source: "gp-config-sync-shipping",
              },
            },
          })),
        },
      })
      createdStockLocations = missingSellers.length
    }
  }

  const locations = await listStockLocationsByNames(db, sellerNames)
  const locationIds = locations.map(location => location.id)
  const locationFulfillmentSets = await listLocationFulfillmentSets(db, locationIds)
  const locationIdsWithFulfillmentSets = new Set(
    locationFulfillmentSets.map(link => link.stock_location_id)
  )
  const locationsMissingFulfillmentSets = locations.filter(
    location => !locationIdsWithFulfillmentSets.has(location.id)
  )

  let createdFulfillmentSets = 0
  if (locationsMissingFulfillmentSets.length > 0) {
    if (dryRun) {
      createdFulfillmentSets = locationsMissingFulfillmentSets.length
    } else {
      for (const location of locationsMissingFulfillmentSets) {
        await createLocationFulfillmentSetWorkflow(container).run({
          input: {
            location_id: location.id,
            fulfillment_set_data: {
              name: `${location.name} fulfillment`,
              type: "shipping",
            },
          },
        })
      }
      createdFulfillmentSets = locationsMissingFulfillmentSets.length
    }
  }

  const refreshedLinks = await listLocationFulfillmentSets(db, locationIds)
  const fulfillmentSetIds = refreshedLinks.map(link => link.fulfillment_set_id)
  const existingZones = await listServiceZonesByFulfillmentSetIds(db, fulfillmentSetIds)
  const fulfillmentSetIdsWithZones = new Set(existingZones.map(zone => zone.fulfillment_set_id))
  const fulfillmentSetIdsMissingZones = fulfillmentSetIds.filter(
    fulfillmentSetId => !fulfillmentSetIdsWithZones.has(fulfillmentSetId)
  )

  let createdServiceZones = 0
  if (fulfillmentSetIdsMissingZones.length > 0) {
    if (dryRun) {
      createdServiceZones = fulfillmentSetIdsMissingZones.length
    } else {
      await createServiceZonesWorkflow(container).run({
        input: {
          data: fulfillmentSetIdsMissingZones.map(fulfillmentSetId => ({
            name: `GP checkout ${fulfillmentSetId}`,
            fulfillment_set_id: fulfillmentSetId,
            geo_zones: countriesForZones.map(countryCode => ({
              type: "country",
              country_code: countryCode.toLowerCase(),
            })),
          })),
        },
      })
      createdServiceZones = fulfillmentSetIdsMissingZones.length
    }
  }

  return {
    createdStockLocations,
    createdFulfillmentSets,
    createdServiceZones,
  }
}

async function listMarketServiceZones(db: Knex, salesChannelId: string): Promise<ServiceZoneRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        sz.id AS service_zone_id,
        scl.stock_location_id,
        sl.name AS stock_location_name
      FROM sales_channel sc
      INNER JOIN sales_channel_stock_location scl
        ON scl.sales_channel_id = sc.id
       AND scl.deleted_at IS NULL
      INNER JOIN stock_location sl
        ON sl.id = scl.stock_location_id
       AND sl.deleted_at IS NULL
      INNER JOIN location_fulfillment_set lfs
        ON lfs.stock_location_id = sl.id
       AND lfs.deleted_at IS NULL
      INNER JOIN service_zone sz
        ON sz.fulfillment_set_id = lfs.fulfillment_set_id
       AND sz.deleted_at IS NULL
      WHERE sc.deleted_at IS NULL
        AND sc.id = ?
      ORDER BY sl.name ASC, sz.id ASC
    `,
    [salesChannelId]
  )

  return extractRows<ServiceZoneRow>(result)
}

async function listSellerIdsByServiceZone(
  db: Knex,
  serviceZoneIds: string[]
): Promise<Map<string, string[]>> {
  if (serviceZoneIds.length === 0) {
    return new Map()
  }

  if (!(await db.schema.hasTable("seller_seller_fulfillment_service_zone"))) {
    return new Map()
  }

  const result = await db.raw(
    `
      SELECT seller_id, service_zone_id
      FROM seller_seller_fulfillment_service_zone
      WHERE deleted_at IS NULL
        AND service_zone_id = ANY(?::text[])
      ORDER BY service_zone_id ASC, seller_id ASC
    `,
    [serviceZoneIds]
  )

  const map = new Map<string, string[]>()
  for (const row of extractRows<SellerZoneRow>(result)) {
    const existing = map.get(row.service_zone_id) ?? []
    existing.push(row.seller_id)
    map.set(row.service_zone_id, existing)
  }

  return map
}

async function listExistingShippingOptions(
  db: Knex,
  shippingProfileIds: string[],
  serviceZoneIds: string[]
): Promise<Map<string, ExistingShippingOptionRow>> {
  if (shippingProfileIds.length === 0 || serviceZoneIds.length === 0) {
    return new Map()
  }

  const result = await db.raw(
    `
      SELECT id, service_zone_id, shipping_profile_id
      FROM shipping_option
      WHERE deleted_at IS NULL
        AND provider_id = ?
        AND shipping_profile_id = ANY(?::text[])
        AND service_zone_id = ANY(?::text[])
      ORDER BY created_at ASC
    `,
    [MANUAL_PROVIDER_ID, shippingProfileIds, serviceZoneIds]
  )

  return new Map(
    extractRows<ExistingShippingOptionRow>(result).map(row => [
      `${row.shipping_profile_id}::${row.service_zone_id}`,
      row,
    ])
  )
}

