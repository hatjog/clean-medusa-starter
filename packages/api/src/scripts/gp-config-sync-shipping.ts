function resolveService(container: any, keysToTry: string[]): any {
  for (const key of keysToTry) {
    try {
      return container.resolve(key)
    } catch {
      // keep trying aliases
    }
  }

  throw new Error(`Unable to resolve service. Tried: ${keysToTry.join(", ")}`)
}

export function parseArgs(args: string[] | undefined): ParsedArgs {
  const instanceId = (args?.[0] ?? process.env.GP_INSTANCE_ID ?? "gp-dev").trim()
  const marketId = (args?.[1] ?? process.env.GP_MARKET_ID ?? "bonbeauty").trim()
  const configRoot = (
    process.env.GP_CONFIG_ROOT ?? path.resolve(process.cwd(), "../config")
  ).trim()
  const dryRun = parseDryRunFlag(args)

  if (!instanceId) throw new Error("instanceId is required (args[0] or GP_INSTANCE_ID)")
  if (!marketId) throw new Error("marketId is required (args[1] or GP_MARKET_ID)")
  if (!configRoot) throw new Error("configRoot is required (GP_CONFIG_ROOT)")

  return { instanceId, marketId, configRoot, dryRun }
}

async function readYamlFile<T>(filePath: string): Promise<T> {
  const raw = await fs.readFile(filePath, "utf8")
  const doc = yaml.load(raw, { schema: yaml.JSON_SCHEMA })

  if (!doc || typeof doc !== "object") {
    throw new Error(`Invalid YAML document: ${filePath}`)
  }

  return doc as T
}

async function loadMarketRuntimeConfig(args: ParsedArgs): Promise<MarketRuntimeConfig> {
  const marketPath = path.resolve(
    args.configRoot,
    args.instanceId,
    "markets",
    args.marketId,
    "market.yaml"
  )

  return readYamlFile<MarketRuntimeConfig>(marketPath)
}

function extractRows<T>(result: any): T[] {
  if (Array.isArray(result?.rows)) return result.rows as T[]
  if (Array.isArray(result)) return result as T[]
  return []
}

function buildSyntheticId(prefix: string): string {
  return `${prefix}_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`
}

function normalizeComparableName(value: string | null | undefined): string {
  return (value ?? "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "")
}

async function listMarketShippingProfiles(db: Knex, marketId: string): Promise<string[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT psp.shipping_profile_id
      FROM product p
      INNER JOIN product_shipping_profile psp
        ON psp.product_id = p.id
      WHERE p.deleted_at IS NULL
        AND p.metadata->'gp'->>'market_id' = ?
      ORDER BY psp.shipping_profile_id ASC
    `,
    [marketId]
  )

  return extractRows<ShippingProfileRow>(result)
    .map(row => row.shipping_profile_id)
    .filter(Boolean)
}

async function listMarketSellers(db: Knex, marketId: string): Promise<MarketSellerRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        ppss.seller_id,
        s.name AS seller_name,
        s.handle AS seller_handle
      FROM product p
      INNER JOIN product_product_seller_seller ppss
        ON ppss.product_id = p.id
       AND ppss.deleted_at IS NULL
      INNER JOIN seller s
        ON s.id = ppss.seller_id
       AND s.deleted_at IS NULL
      WHERE p.deleted_at IS NULL
        AND p.metadata->'gp'->>'market_id' = ?
        AND s.status = 'open'
      ORDER BY s.name ASC
    `,
    [marketId]
  )

  return extractRows<MarketSellerRow>(result)
}

async function listServiceZoneBackedStockLocations(db: Knex): Promise<ServiceZoneRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        sz.id AS service_zone_id,
        lfs.stock_location_id,
        sl.name AS stock_location_name
      FROM location_fulfillment_set lfs
      INNER JOIN stock_location sl
        ON sl.id = lfs.stock_location_id
       AND sl.deleted_at IS NULL
      INNER JOIN service_zone sz
        ON sz.fulfillment_set_id = lfs.fulfillment_set_id
       AND sz.deleted_at IS NULL
      WHERE lfs.deleted_at IS NULL
      ORDER BY sl.name ASC, sz.id ASC
    `
  )

  return extractRows<ServiceZoneRow>(result)
}

async function listStockLocationsByNames(db: Knex, names: string[]): Promise<StockLocationRow[]> {
  if (names.length === 0) {
    return []
  }

  return db("stock_location")
    .select("id", "name")
    .whereNull("deleted_at")
    .whereIn("name", names)
    .orderBy("name", "asc") as Promise<StockLocationRow[]>
}

