async function ensureSellerShippingOptionLink(
  db: Knex,
  sellerId: string,
  shippingOptionId: string,
  dryRun: boolean
): Promise<"created" | "existing"> {
  const existing = await db("seller_seller_fulfillment_shipping_option")
    .select("seller_id")
    .where({ seller_id: sellerId, shipping_option_id: shippingOptionId })
    .whereNull("deleted_at")
    .first()

  if (existing) {
    return "existing"
  }

  if (dryRun) {
    return "created"
  }

  await db("seller_seller_fulfillment_shipping_option").insert({
    seller_id: sellerId,
    shipping_option_id: shippingOptionId,
    id: buildSyntheticId("ssfsso"),
  })

  return "created"
}

async function pruneSellerShippingOptionLinks(
  db: Knex,
  shippingOptionId: string,
  allowedSellerIds: string[],
  dryRun: boolean
): Promise<number> {
  const existing = await db("seller_seller_fulfillment_shipping_option")
    .select("id", "seller_id")
    .where({ shipping_option_id: shippingOptionId })
    .whereNull("deleted_at")

  const allowedSellerIdSet = new Set(allowedSellerIds)
  const staleLinks = (existing as SellerShippingOptionLinkRow[]).filter(
    row => !allowedSellerIdSet.has(row.seller_id)
  )

  if (staleLinks.length === 0) {
    return 0
  }

  if (dryRun) {
    return staleLinks.length
  }

  await db("seller_seller_fulfillment_shipping_option")
    .whereIn(
      "id",
      staleLinks.map(row => row.id)
    )
    .update({ deleted_at: db.fn.now() })

  return staleLinks.length
}

async function resolveShippingOptionTypeId(db: Knex, fulfillmentService: any, dryRun: boolean) {
  const existing = await db("shipping_option_type")
    .select("id")
    .where({ code: OPTION_TYPE_CODE })
    .whereNull("deleted_at")
    .first<{ id: string }>()

  if (existing?.id) {
    return { id: existing.id, created: false }
  }

  if (dryRun) {
    return { id: `dryrun-${OPTION_TYPE_CODE}`, created: true }
  }

  const created = await fulfillmentService.createShippingOptionTypes({
    label: OPTION_TYPE_LABEL,
    code: OPTION_TYPE_CODE,
    description: OPTION_TYPE_DESCRIPTION,
  })

  return { id: created.id as string, created: true }
}

export default async function gpConfigSyncShipping({ container, args }: ExecArgs) {
  const parsedArgs = parseArgs(args)
  const marketConfig = await loadMarketRuntimeConfig(parsedArgs)
  const db = container.resolve(ContainerRegistrationKeys.PG_CONNECTION) as Knex
  const fulfillmentService = resolveService(container, [
    "fulfillment",
    "fulfillmentModuleService",
    "IFulfillmentModuleService",
    "fulfillment_module",
  ])

  const currency = (marketConfig.currency ?? "").trim()
  const countries = Array.isArray(marketConfig.countries) ? marketConfig.countries : []
  if (!currency) {
    throw new Error("market.currency is required to bootstrap shipping options")
  }

  const prereqs = await validatePrerequisites(container, parsedArgs.marketId, currency, [])

  const [marketSellers, serviceZoneBackedStockLocations] = await Promise.all([
    listMarketSellers(db, parsedArgs.marketId),
    listServiceZoneBackedStockLocations(db),
  ])
  const activeMarketSellerIds = new Set(marketSellers.map(seller => seller.seller_id))

  const matchedSellerIdsByServiceZone = new Map<string, string[]>()
  const matchedStockLocationIds = new Set<string>()

  for (const seller of marketSellers) {
    const sellerKeys = [
      normalizeComparableName(seller.seller_name),
      normalizeComparableName(seller.seller_handle),
    ].filter(Boolean)

    for (const location of serviceZoneBackedStockLocations) {
      const normalizedLocationName = normalizeComparableName(location.stock_location_name)
      if (!normalizedLocationName || !sellerKeys.includes(normalizedLocationName)) {
        continue
      }

      matchedStockLocationIds.add(location.stock_location_id)
      const existing = matchedSellerIdsByServiceZone.get(location.service_zone_id) ?? []
      if (!existing.includes(seller.seller_id)) {
        existing.push(seller.seller_id)
        matchedSellerIdsByServiceZone.set(location.service_zone_id, existing)
      }
    }
  }

  let createdSalesChannelLinks = 0
  for (const stockLocationId of matchedStockLocationIds) {
    const linkStatus = await ensureSalesChannelStockLocationLink(
      db,
      prereqs.salesChannelId,
      stockLocationId,
      parsedArgs.dryRun
    )

    if (linkStatus === "created") {
      createdSalesChannelLinks += 1
    }
  }

  const [regions, shippingProfileIds, serviceZones] = await Promise.all([
    listRegionsWithCountries(db),
    listMarketShippingProfiles(db, parsedArgs.marketId),
    listMarketServiceZones(db, prereqs.salesChannelId),
  ])

  const region = selectRegionForMarket(regions, currency, countries)

  if (shippingProfileIds.length === 0) {
    console.warn(`No shipping profiles found for market '${parsedArgs.marketId}'. Skipping shipping sync.`)
    return
  }

  if (serviceZones.length === 0) {
    console.warn(`No service zones found for market '${parsedArgs.marketId}'. Skipping shipping sync.`)
    return
  }

  const { id: shippingOptionTypeId, created: createdType } = await resolveShippingOptionTypeId(
    db,
    fulfillmentService,
    parsedArgs.dryRun
  )

  const sellerIdsByServiceZone = await listSellerIdsByServiceZone(
    db,
    serviceZones.map(zone => zone.service_zone_id)
  )

  for (const [serviceZoneId, sellerIds] of sellerIdsByServiceZone.entries()) {
    sellerIdsByServiceZone.set(
      serviceZoneId,
      sellerIds.filter(sellerId => activeMarketSellerIds.has(sellerId))
    )
  }

  for (const [serviceZoneId, sellerIds] of matchedSellerIdsByServiceZone.entries()) {
    const existing = sellerIdsByServiceZone.get(serviceZoneId) ?? []
    for (const sellerId of sellerIds) {
      if (activeMarketSellerIds.has(sellerId) && !existing.includes(sellerId)) {
        existing.push(sellerId)
      }
    }
    sellerIdsByServiceZone.set(serviceZoneId, existing)
  }
  const existingOptions = await listExistingShippingOptions(
    db,
    shippingProfileIds,
    serviceZones.map(zone => zone.service_zone_id)
  )

  let createdOptions = 0
  let reusedOptions = 0
  let createdLocationLinks = 0
  let createdSellerLinks = 0
  let prunedSellerLinks = 0

  for (const zone of serviceZones) {
    const locationLinkStatus = await ensureLocationProviderLink(
      db,
      zone.stock_location_id,
      parsedArgs.dryRun
    )
    if (locationLinkStatus === "created") {
      createdLocationLinks += 1
    }

    for (const shippingProfileId of shippingProfileIds) {
      const key = `${shippingProfileId}::${zone.service_zone_id}`
      let shippingOptionId = existingOptions.get(key)?.id ?? null

      if (!shippingOptionId) {
        if (parsedArgs.dryRun) {
          shippingOptionId = `dryrun-${shippingProfileId}-${zone.service_zone_id}`
        } else {
          const { result } = await createShippingOptionsWorkflow(container).run({
            input: [
              {
                name: `${parsedArgs.marketId} checkout`,
                price_type: "flat",
                service_zone_id: zone.service_zone_id,
                shipping_profile_id: shippingProfileId,
                provider_id: MANUAL_PROVIDER_ID,
                type: shippingOptionTypeId,
                prices: [
                  {
                    region_id: region.id,
                    amount: 0,
                  },
                ],
                rules: [
                  {
                    attribute: "enabled_in_store",
                    operator: "eq",
                    value: "true",
                  },
                  {
                    attribute: "is_return",
                    operator: "eq",
                    value: "false",
                  },
                ],
                data: {
                  gp_market_id: parsedArgs.marketId,
                },
                metadata: {
                  gp: {
                    market_id: parsedArgs.marketId,
                    autogenerated: true,
                    source: "gp-config-sync-shipping",
                  },
                },
              },
            ],
          })

          shippingOptionId = result[0]?.id as string
        }

        createdOptions += 1
      } else {
        reusedOptions += 1
      }

      const allowedSellerIds = sellerIdsByServiceZone.get(zone.service_zone_id) ?? []

      for (const sellerId of allowedSellerIds) {
        const sellerLinkStatus = await ensureSellerShippingOptionLink(
          db,
          sellerId,
          shippingOptionId,
          parsedArgs.dryRun
        )

        if (sellerLinkStatus === "created") {
          createdSellerLinks += 1
        }
      }

      prunedSellerLinks += await pruneSellerShippingOptionLinks(
        db,
        shippingOptionId,
        allowedSellerIds,
        parsedArgs.dryRun
      )
    }
  }

  console.log(
    JSON.stringify(
      {
        ok: true,
        dry_run: parsedArgs.dryRun,
        instance_id: parsedArgs.instanceId,
        market_id: parsedArgs.marketId,
        shipping_option_type_created: createdType,
        created_options: createdOptions,
        reused_options: reusedOptions,
        created_sales_channel_stock_location_links: createdSalesChannelLinks,
        created_location_provider_links: createdLocationLinks,
        created_seller_shipping_links: createdSellerLinks,
        pruned_seller_shipping_links: prunedSellerLinks,
      },
      null,
      2
    )
  )
}