const MANUAL_PROVIDER_ID = "manual_manual"
const OPTION_TYPE_CODE = "gp-checkout"
const OPTION_TYPE_LABEL = "GP Checkout"
const OPTION_TYPE_DESCRIPTION = "Autogenerated storefront checkout shipping option"

type ParsedArgs = {
  instanceId: string
  marketId: string
  configRoot: string
  dryRun: boolean
}

type MarketRuntimeConfig = {
  currency?: string
  countries?: string[]
}

type ShippingProfileRow = {
  shipping_profile_id: string
}

type ServiceZoneRow = {
  service_zone_id: string
  stock_location_id: string
  stock_location_name: string | null
}

type SellerStockLocationRow = {
  stock_location_id: string
}

type SellerZoneRow = {
  seller_id: string
  service_zone_id: string
}

type MarketSellerRow = {
  seller_id: string
  seller_name: string
  seller_handle: string
}

type ExistingShippingOptionRow = {
  id: string
  service_zone_id: string
  shipping_profile_id: string
}

type SellerShippingOptionLinkRow = {
  id: string
  seller_id: string
}

function resolveService(container: any, keysToTry: string[]): any {
  for (const key of keysToTry) {
    try {
      return container.resolve(key)
    } catch {
      // keep trying aliases
    }
  }

  throw new Error(`Unable to resolve service. Tried: ${keysToTry.join(", ")}`)
}

export function parseArgs(args: string[] | undefined): ParsedArgs {
  const instanceId = (args?.[0] ?? process.env.GP_INSTANCE_ID ?? "gp-dev").trim()
  const marketId = (args?.[1] ?? process.env.GP_MARKET_ID ?? "bonbeauty").trim()
  const configRoot = (
    process.env.GP_CONFIG_ROOT ?? path.resolve(process.cwd(), "../config")
  ).trim()
  const dryRun = parseDryRunFlag(args)

  if (!instanceId) throw new Error("instanceId is required (args[0] or GP_INSTANCE_ID)")
  if (!marketId) throw new Error("marketId is required (args[1] or GP_MARKET_ID)")
  if (!configRoot) throw new Error("configRoot is required (GP_CONFIG_ROOT)")

  return { instanceId, marketId, configRoot, dryRun }
}

async function readYamlFile<T>(filePath: string): Promise<T> {
  const raw = await fs.readFile(filePath, "utf8")
  const doc = yaml.load(raw, { schema: yaml.JSON_SCHEMA })

  if (!doc || typeof doc !== "object") {
    throw new Error(`Invalid YAML document: ${filePath}`)
  }

  return doc as T
}

async function loadMarketRuntimeConfig(args: ParsedArgs): Promise<MarketRuntimeConfig> {
  const marketPath = path.resolve(
    args.configRoot,
    args.instanceId,
    "markets",
    args.marketId,
    "market.yaml"
  )

  return readYamlFile<MarketRuntimeConfig>(marketPath)
}

function extractRows<T>(result: any): T[] {
  if (Array.isArray(result?.rows)) return result.rows as T[]
  if (Array.isArray(result)) return result as T[]
  return []
}

function buildSyntheticId(prefix: string): string {
  return `${prefix}_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`
}

function normalizeComparableName(value: string | null | undefined): string {
  return (value ?? "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "")
}

async function listMarketShippingProfiles(db: Knex, marketId: string): Promise<string[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT psp.shipping_profile_id
      FROM product p
      INNER JOIN product_shipping_profile psp
        ON psp.product_id = p.id
      WHERE p.deleted_at IS NULL
        AND p.metadata->'gp'->>'market_id' = ?
      ORDER BY psp.shipping_profile_id ASC
    `,
    [marketId]
  )

  return extractRows<ShippingProfileRow>(result)
    .map(row => row.shipping_profile_id)
    .filter(Boolean)
}

async function listMarketSellers(db: Knex, marketId: string): Promise<MarketSellerRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        sspp.seller_id,
        s.name AS seller_name,
        s.handle AS seller_handle
      FROM product p
      INNER JOIN seller_seller_product_product sspp
        ON sspp.product_id = p.id
       AND sspp.deleted_at IS NULL
      INNER JOIN seller s
        ON s.id = sspp.seller_id
       AND s.deleted_at IS NULL
      WHERE p.deleted_at IS NULL
        AND p.metadata->'gp'->>'market_id' = ?
        AND s.store_status = 'ACTIVE'
      ORDER BY s.name ASC
    `,
    [marketId]
  )

  return extractRows<MarketSellerRow>(result)
}

async function listServiceZoneBackedStockLocations(db: Knex): Promise<ServiceZoneRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        sz.id AS service_zone_id,
        lfs.stock_location_id,
        sl.name AS stock_location_name
      FROM location_fulfillment_set lfs
      INNER JOIN stock_location sl
        ON sl.id = lfs.stock_location_id
       AND sl.deleted_at IS NULL
      INNER JOIN service_zone sz
        ON sz.fulfillment_set_id = lfs.fulfillment_set_id
       AND sz.deleted_at IS NULL
      WHERE lfs.deleted_at IS NULL
      ORDER BY sl.name ASC, sz.id ASC
    `
  )

  return extractRows<ServiceZoneRow>(result)
}

async function listMarketServiceZones(db: Knex, salesChannelId: string): Promise<ServiceZoneRow[]> {
  const result = await db.raw(
    `
      SELECT DISTINCT
        sz.id AS service_zone_id,
        scl.stock_location_id,
        sl.name AS stock_location_name
      FROM sales_channel sc
      INNER JOIN sales_channel_stock_location scl
        ON scl.sales_channel_id = sc.id
       AND scl.deleted_at IS NULL
      INNER JOIN stock_location sl
        ON sl.id = scl.stock_location_id
       AND sl.deleted_at IS NULL
      INNER JOIN location_fulfillment_set lfs
        ON lfs.stock_location_id = sl.id
       AND lfs.deleted_at IS NULL
      INNER JOIN service_zone sz
        ON sz.fulfillment_set_id = lfs.fulfillment_set_id
       AND sz.deleted_at IS NULL
      WHERE sc.deleted_at IS NULL
        AND sc.id = ?
      ORDER BY sl.name ASC, sz.id ASC
    `,
    [salesChannelId]
  )

  return extractRows<ServiceZoneRow>(result)
}

async function listSellerIdsByServiceZone(
  db: Knex,
  serviceZoneIds: string[]
): Promise<Map<string, string[]>> {
  if (serviceZoneIds.length === 0) {
    return new Map()
  }

  const result = await db.raw(
    `
      SELECT seller_id, service_zone_id
      FROM seller_seller_fulfillment_service_zone
      WHERE deleted_at IS NULL
        AND service_zone_id = ANY(?::text[])
      ORDER BY service_zone_id ASC, seller_id ASC
    `,
    [serviceZoneIds]
  )

  const map = new Map<string, string[]>()
  for (const row of extractRows<SellerZoneRow>(result)) {
    const existing = map.get(row.service_zone_id) ?? []
    existing.push(row.seller_id)
    map.set(row.service_zone_id, existing)
  }

  return map
}

async function listExistingShippingOptions(
  db: Knex,
  shippingProfileIds: string[],
  serviceZoneIds: string[]
): Promise<Map<string, ExistingShippingOptionRow>> {
  if (shippingProfileIds.length === 0 || serviceZoneIds.length === 0) {
    return new Map()
  }

  const result = await db.raw(
    `
      SELECT id, service_zone_id, shipping_profile_id
      FROM shipping_option
      WHERE deleted_at IS NULL
        AND provider_id = ?
        AND shipping_profile_id = ANY(?::text[])
        AND service_zone_id = ANY(?::text[])
      ORDER BY created_at ASC
    `,
    [MANUAL_PROVIDER_ID, shippingProfileIds, serviceZoneIds]
  )

  return new Map(
    extractRows<ExistingShippingOptionRow>(result).map(row => [
      `${row.shipping_profile_id}::${row.service_zone_id}`,
      row,
    ])
  )
}

async function ensureLocationProviderLink(
  db: Knex,
  stockLocationId: string,
  dryRun: boolean
): Promise<"created" | "existing"> {
  const existing = await db("location_fulfillment_provider")
    .select("stock_location_id")
    .where({
      stock_location_id: stockLocationId,
      fulfillment_provider_id: MANUAL_PROVIDER_ID,
    })
    .whereNull("deleted_at")
    .first()

  if (existing) {
    return "existing"
  }

  if (dryRun) {
    return "created"
  }

  await db("location_fulfillment_provider").insert({
    stock_location_id: stockLocationId,
    fulfillment_provider_id: MANUAL_PROVIDER_ID,
    id: buildSyntheticId("locfp"),
  })

  return "created"
}

